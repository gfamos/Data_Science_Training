#Addition (+)
a = 10
b = 20
print(a + b)
print(10 + 20)


#Subtraction
print(b - a)

#Multiplication
print(a * b)

#Division
print(b / a) #Note that the output of division is a float

#Modulo
print(b % a) #prints the reminder of division
print(21 % a)

#Floor Division(//)
print(10.24 / 2)
print(10.24 // 2)

print(-3 / 2)
print(-3 // 2)

#Exponent Operator of Power Operator(**)

print(10 ** 2)