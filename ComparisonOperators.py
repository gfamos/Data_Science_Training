"""Comparison Operators"""
# Are used to evaluate two values and return the boolean judgement based on the evaluation


# == - Equal to
print(10 == 10)
print(10 == 20)

# != - Not Equals to
print(10 != 10)
print(10 != 20)

# > - Greater than
print(10 > 10)
print(10 > 20)

# < - Less than
print(10 < 10)
print(10 < 20)

# >= - Greater than or Equals to
print(10 >= 10)
print(10 >= 20)

# <= - Lesser than or Equals to
print(10 <= 10)
print(10 <= 20)
