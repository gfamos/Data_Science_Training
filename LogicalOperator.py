"""Logical Operators"""
# Are used to perform logical test on boolean values


# and
a = True
b = False

print(a and b) # Both has to be true to get true resul
print((10 > 20) and b)

# or
print(a or b) # Directly opposite to and. Anyone of the values has to be true
print(a or (5 > 10))

# not
print((not a))